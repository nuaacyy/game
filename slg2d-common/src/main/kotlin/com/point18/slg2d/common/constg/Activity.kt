// 根据奇观配置Id获得积分Condition Id
fun getWonderOccupyCondition(wonderId: Int): Int {
    return when (wonderId) {
        1 -> BIG_WONDER_OCCUPY_TIME
        2 -> GREEN_WONDER_OCCUPY_TIME
        3 -> BLUE_WONDER_OCCUPY_TIME
        4 -> RED_WONDER_OCCUPY_TIME
        5 -> PURPLE_WONDER_OCCUPY_TIME
        6 -> GOLD_WONDER_OCCUPY_TIME
        7 -> AIR_WONDER_OCCUPY_TIME
        else -> -1 // 参数错误
    }
}

// 根据兵种阶级获得积分Condition Id
fun getKillSoliderCondition(isLeader: Boolean, walkType: Int, soliderStep: Int): Int {
    return if(walkType == com.point18.slg2d.common.constg.WalkOccupyWonder) {
        if (isLeader) {
            when (soliderStep) {
                1 -> WONDER_LEADER_KILL_SOLIDER_1
                2 -> WONDER_LEADER_KILL_SOLIDER_2
                3 -> WONDER_LEADER_KILL_SOLIDER_3
                4 -> WONDER_LEADER_KILL_SOLIDER_4
                else -> -1
            }
        } else {
            when (soliderStep) {
                1 -> WONDER_KILL_SOLIDER_1
                2 -> WONDER_KILL_SOLIDER_2
                3 -> WONDER_KILL_SOLIDER_3
                4 -> WONDER_KILL_SOLIDER_4
                else -> -1
            }
        }
    } else {
        when (soliderStep) {
            1 -> KILL_SOLIDER_1
            2 -> KILL_SOLIDER_2
            3 -> KILL_SOLIDER_3
            4 -> KILL_SOLIDER_4
            else -> -1
        }
    }
}

// 根据兵种阶级获得积分Condition Id
fun getLoseSoliderCondition(isLeader: Boolean, walkType: Int, soliderStep: Int): Int {
    return if(walkType == WalkOccupyWonder) {
        if (isLeader) {
            when (soliderStep) {
                1 -> WONDER_LEADER_LOSE_SOLIDER_1
                2 -> WONDER_LEADER_LOSE_SOLIDER_2
                3 -> WONDER_LEADER_LOSE_SOLIDER_3
                4 -> WONDER_LEADER_LOSE_SOLIDER_4
                else -> -1
            }
        } else {
            when (soliderStep) {
                1 -> WONDER_LOSE_SOLIDER_1
                2 -> WONDER_LOSE_SOLIDER_2
                3 -> WONDER_LOSE_SOLIDER_3
                4 -> WONDER_LOSE_SOLIDER_4
                else -> -1
            }
        }
    } else {
        when (soliderStep) {
            1 -> LOSE_SOLIDER_1
            2 -> LOSE_SOLIDER_2
            3 -> LOSE_SOLIDER_3
            4 -> LOSE_SOLIDER_4
            else -> -1
        }
    }
}

